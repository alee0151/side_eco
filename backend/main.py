def clean_text(value: Optional[str]) -> Optional[str]:
    """Strip whitespace; normalise empty strings to None."""
    if value is None:
        return None
    value = value.strip()
    return value if value else None


def clean_abn(value: str) -> str:
    """Remove spaces from an ABN string (e.g. '12 345 678 901' -> '12345678901')."""
    return re.sub(r"\s+", "", value or "")


def is_abn(value: str) -> bool:
    """Return True if value is exactly 11 digits after whitespace removal."""
    return clean_abn(value).isdigit() and len(clean_abn(value)) == 11


def get_single_input_type(
    barcode:        Optional[str],
    brand:          Optional[str],
    company_or_abn: Optional[str],
) -> Tuple[str, str, str]:
    """
    Enforce exactly one input type per request.

    Returns (input_type, input_value, frontend_type) where:
    - input_type   matches the database search_query.input_type_enum value
    - input_value  is the cleaned string stored in search_query.input_value
    - frontend_type is returned to the frontend in the response

    Raises HTTP 400 when zero or more than one inputs are provided, or when
    the provided value fails basic format validation.
    """
    provided = []
    if barcode:        provided.append(("barcode",      barcode,        "barcode"))
    if brand:          provided.append(("brand_name",   brand,          "brand"))
    if company_or_abn: provided.append(("company_name", company_or_abn, "company_or_abn"))

    if len(provided) == 0:
        raise HTTPException(
            status_code=400,
            detail="Please provide exactly one input: barcode, brand, or company_or_abn.",
        )
    if len(provided) > 1:
        raise HTTPException(
            status_code=400,
            detail="Please submit only one input type per search.",
        )

    input_type, input_value, frontend_type = provided[0]

    # Barcode: delegate deep validation to barcode_pipeline (EAN-13 checksum).
    # Basic numeric check here just ensures we don't hit the pipeline with garbage.
    if input_type == "barcode":
        cleaned_bc = re.sub(r"[\s\-]", "", input_value)
        if not cleaned_bc.isdigit() or not (8 <= len(cleaned_bc) <= 14):
            raise HTTPException(
                status_code=400,
                detail="Invalid barcode. Must be 8-14 digits (EAN-8, EAN-13, ITF-14).",
            )

    if input_type == "brand_name" and len(input_value) < 2:
        raise HTTPException(
            status_code=400,
            detail="Brand name must contain at least 2 characters.",
        )

    if input_type == "company_name":
        cleaned = clean_abn(input_value)
        if cleaned.isdigit() and not is_abn(cleaned):
            raise HTTPException(status_code=400, detail="Invalid ABN. ABN must be 11 digits.")
        if not cleaned.isdigit() and len(input_value) < 2:
            raise HTTPException(
                status_code=400,
                detail="Company name must contain at least 2 characters.",
            )

    return input_type, input_value, frontend_type


