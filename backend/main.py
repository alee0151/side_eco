@app.post("/api/search")
def search_entity(payload: SearchRequest):
    """
    Main consumer search endpoint.

    Responsibilities
    ----------------
    1. Validate exactly one input is provided.
    2. Create a pending search_query record (returns query_id immediately).
    3. Dispatch to the appropriate pipeline module by input type.
    4. Update search_query to resolved / failed.
    5. Return a unified JSON response to the frontend.

    Input types and their pipelines
    --------------------------------
    barcode        -> barcode_pipeline.run_barcode_phase()
    brand_name     -> brand_pipeline.run_brand_phase()
    company_name   -> ABR inline (simple XML call, no dedicated module needed)
    """
    barcode        = clean_text(payload.barcode)
    brand          = clean_text(payload.brand)
    company_or_abn = clean_text(payload.company_or_abn)

    input_type, input_value, frontend_type = get_single_input_type(
        barcode, brand, company_or_abn
    )

    conn = get_conn()
    cur  = conn.cursor()

    try:
        query    = create_search_query(cur, input_type, input_value, payload.user_id)
        query_id = query["query_id"]

        pipeline_steps: List[str] = []
        result:         Dict[str, Any] = {}
        db_status = "failed"

        # ----------------------------------------------------
        # Branch 1 — Company name / ABN
        # Simple ABR lookup, no dedicated module needed.
        # ----------------------------------------------------
        if input_type == "company_name":
            if is_abn(company_or_abn):
                abn = clean_abn(company_or_abn)
                pipeline_steps.append("ABR ABN verification")
                abn_result = verify_abn_with_abr(abn)
                db_status  = "resolved" if abn_result.get("success") else "failed"
                result = {
                    "input_type":       "abn",
                    "input_value":      abn,
                    "status":           "external_resolved" if abn_result.get("success") else "not_found",
                    "source":           "ABR",
                    "company": {
                        "legal_name":    abn_result.get("legal_name"),
                        "abn":           abn_result.get("abn"),
                        "state":         abn_result.get("state"),
                        "postcode":      abn_result.get("postcode"),
                        "abn_status":    abn_result.get("abn_status"),
                        "gst_registered": abn_result.get("gst_registered"),
                    },
                    "abn_verification": abn_result,
                    "confidence":       95 if abn_result.get("success") else 0,
                }
            else:
                pipeline_steps.append("ABR company name search")
                abr_result = search_company_name_with_abr(company_or_abn)
                db_status  = "resolved" if abr_result.get("success") else "failed"
                result = {
                    "input_type":       "company_name",
                    "input_value":      company_or_abn,
                    "status":           "external_resolved" if abr_result.get("success") else "not_found",
                    "source":           "ABR",
                    "company": {
                        "legal_name":  abr_result.get("legal_name"),
                        "abn":         abr_result.get("abn"),
                        "state":       abr_result.get("state"),
                        "postcode":    abr_result.get("postcode"),
                        "abn_status":  abr_result.get("abn_status"),
                    },
                    "abn_verification": abr_result,
                    "message":          abr_result.get("message"),
                    "confidence":       90 if abr_result.get("success") else 0,
                }

            update_search_query(cur, query_id, db_status)

        # ----------------------------------------------------
        # Branch 2 — Barcode
        # Delegated entirely to barcode_pipeline.py.
        # Flow: EAN-13 validate -> OpenFoodFacts -> GS1 fallback -> ABR
        # ----------------------------------------------------
        elif input_type == "barcode":
            barcode_result = run_barcode_phase(
                barcode,
                abr_lookup_fn=search_company_name_with_abr,
            )
            pipeline_steps = barcode_result.get("pipeline", [])
            db_status      = "resolved" if barcode_result["success"] else "failed"

            result = {
                "input_type":       "barcode",
                "input_value":      barcode,
                "status":           barcode_result.get("status", "not_found"),
                "source":           barcode_result.get("source"),
                "product":          barcode_result.get("product"),
                "brand_raw":        barcode_result.get("brand_raw"),
                "brand_clean":      barcode_result.get("brand_clean"),
                "brand_owner":      barcode_result.get("brand_owner"),
                "manufacturer":     barcode_result.get("manufacturer"),
                "abn_verification": barcode_result.get("abr"),
                "confidence":       barcode_result.get("confidence", 0),
                "errors":           barcode_result.get("errors", []),
                "message":          barcode_result.get("error"),
            }

            update_search_query(cur, query_id, db_status)

        # ----------------------------------------------------
        # Branch 3 — Brand name
        # Delegated entirely to brand_pipeline.py.
        # Flow: IP Australia Trade Mark Search -> extract owner -> ABR
        # ----------------------------------------------------
        elif input_type == "brand_name":
            brand_result   = run_brand_phase(
                brand,
                abr_lookup_fn=search_company_name_with_abr,
            )
            pipeline_steps = brand_result.get("pipeline", [])
            db_status      = "resolved" if brand_result["success"] else "failed"

            result = {
                "input_type":       "brand",
                "input_value":      brand,
                "status":           brand_result.get("status", "not_found"),
                "source":           "IP Australia Trade Mark + ABR",
                "brand_name":       brand,
                "trademark":        brand_result.get("trademark"),
                "legal_owner":      brand_result.get("legal_owner"),
                "abn_verification": brand_result.get("abr"),
                "confidence":       brand_result.get("confidence", 0),
                "errors":           brand_result.get("errors", []),
                "message":          brand_result.get("error"),
            }

            update_search_query(cur, query_id, db_status)

        conn.commit()

        return {
            "query_id":          str(query_id),
            "status":            "success",
            "input_type":        frontend_type,
            "input_value":       input_value,
            "resolution_status": db_status,
            "pipeline_steps":    pipeline_steps,
            "result":            result,
        }

    except HTTPException:
        conn.rollback()
        raise
    except Exception as e:
        conn.rollback()
        raise HTTPException(status_code=500, detail=str(e))
    finally:
        cur.close()
        conn.close()


