# The company/ABN pipeline is self-contained here because it uses a simple
# XML call with no OAuth or multi-step resolution.  The barcode and brand
# pipelines are more complex and live in their own modules.

def verify_abn_with_abr(abn: str) -> Dict[str, Any]:
    """
    Verify an ABN via ABR SearchByABNv202001.

    Required .env: ABR_GUID
    """
    guid = os.getenv("ABR_GUID")
    if not guid:
        return {"success": False, "source": "ABR", "message": "ABR_GUID missing in .env"}

    abn = clean_abn(abn)
    if not is_abn(abn):
        return {"success": False, "source": "ABR", "message": "ABN must be 11 digits"}

    url = "https://abr.business.gov.au/abrxmlsearch/AbrXmlSearch.asmx/SearchByABNv202001"
    params = {
        "searchString": abn,
        "includeHistoricalDetails": "N",
        "authenticationGuid": guid,
    }

    try:
        response = requests.get(url, params=params, timeout=15)
        response.raise_for_status()

        root = ET.fromstring(response.text)
        ns   = {"abr": "http://abr.business.gov.au/ABRXMLSearch/"}

        # Check for ABR-level XML error before parsing results.
        exception_node = root.find(".//abr:exception", ns)
        if exception_node is not None:
            desc = exception_node.find(".//abr:exceptionDescription", ns)
            return {"success": False, "source": "ABR",
                    "message": desc.text if desc is not None else "ABR returned an exception"}

        abn_node      = root.find(".//abr:identifierValue", ns)
        name_node     = root.find(".//abr:organisationName", ns)
        state_node    = root.find(".//abr:stateCode", ns)
        postcode_node = root.find(".//abr:postcode", ns)
        status_node   = root.find(".//abr:entityStatusCode", ns)
        gst_node      = root.find(".//abr:goodsAndServicesTax", ns)

        if abn_node is None:
            return {"success": False, "source": "ABR", "message": "ABN not found in ABR"}

        return {
            "success":       True,
            "source":        "ABR",
            "abn":           abn_node.text,
            "legal_name":    name_node.text if name_node is not None else None,
            "state":         state_node.text if state_node is not None else None,
            "postcode":      postcode_node.text if postcode_node is not None else None,
            "abn_status":    status_node.text if status_node is not None else None,
            "gst_registered": gst_node is not None,
            "verified":      True,
        }

    except requests.exceptions.Timeout:
        return {"success": False, "source": "ABR", "message": "ABR request timed out"}
    except requests.exceptions.RequestException as e:
        return {"success": False, "source": "ABR", "message": f"Network error: {e}"}
    except ET.ParseError as e:
        return {"success": False, "source": "ABR", "message": f"Invalid XML from ABR: {e}"}
    except Exception as e:
        return {"success": False, "source": "ABR", "message": f"Unexpected error: {e}"}


def search_company_name_with_abr(company_name: str) -> Dict[str, Any]:
    """
    Search ABR by company / business name.

    Used by:
    - The company_name pipeline branch (direct user input)
    - barcode_pipeline.py  (passed as abr_lookup_fn)
    - brand_pipeline.py    (passed as abr_lookup_fn)

    Required .env: ABR_GUID
    """
    guid = os.getenv("ABR_GUID")
    if not guid:
        return {"success": False, "source": "ABR", "message": "ABR_GUID missing in .env"}

    if not company_name or len(company_name.strip()) < 2:
        return {"success": False, "source": "ABR", "message": "Company name is too short"}

    url = "https://abr.business.gov.au/abrxmlsearch/AbrXmlSearch.asmx/ABRSearchByNameAdvancedSimpleProtocol2017"
    params = {
        "name":               company_name.strip(),
        "postcode":           "",
        "legalName":          "Y",
        "tradingName":        "Y",
        "NSW": "Y", "SA": "Y", "ACT": "Y", "VIC": "Y",
        "WA":  "Y", "NT":  "Y", "QLD": "Y", "TAS": "Y",
        "authenticationGuid": guid,
    }

    try:
        response = requests.get(url, params=params, timeout=20)
        response.raise_for_status()

        root = ET.fromstring(response.text)
        ns   = {"abr": "http://abr.business.gov.au/ABRXMLSearch/"}

        # Check for ABR-level XML error.
        exception_node = root.find(".//abr:exception", ns)
        if exception_node is not None:
            desc = exception_node.find(".//abr:exceptionDescription", ns)
            return {"success": False, "source": "ABR",
                    "message": desc.text if desc is not None else "ABR returned an exception"}

        businesses = root.findall(".//abr:businessEntity", ns)
        if not businesses:
            return {"success": False, "source": "ABR", "message": "No company found in ABR"}

        # Collect all results and deduplicate by ABN.
        results = []
        seen_abns: set = set()
        for business in businesses:
            abn_node    = business.find(".//abr:identifierValue", ns)
            state_node  = business.find(".//abr:stateCode", ns)
            post_node   = business.find(".//abr:postcode", ns)
            status_node = business.find(".//abr:entityStatusCode", ns)
            abn_val     = abn_node.text if abn_node is not None else None

            if abn_val in seen_abns:
                continue
            seen_abns.add(abn_val)

            # Company name can be under organisationName or givenName/familyName.
            name_node = business.find(".//abr:organisationName", ns)
            if name_node is not None:
                legal_name = name_node.text
            else:
                given  = business.find(".//abr:givenName", ns)
                family = business.find(".//abr:familyName", ns)
                legal_name = " ".join(
                    filter(None, [
                        given.text if given is not None else None,
                        family.text if family is not None else None,
                    ])
                ) or company_name

            results.append({
                "abn":        abn_val,
                "legal_name": legal_name,
                "state":      state_node.text if state_node is not None else None,
                "postcode":   post_node.text if post_node is not None else None,
                "abn_status": status_node.text if status_node is not None else None,
                "verified":   abn_val is not None,
            })

        # Prefer an Active entity with a verified ABN.
        best = next(
            (r for r in results if r.get("abn_status") == "Active" and r["abn"]),
            results[0],
        )

        return {
            "success":     True,
            "source":      "ABR",
            **best,
            "best_match":  best,
            "all_results": results,
            "total":       len(results),
        }

    except requests.exceptions.Timeout:
        return {"success": False, "source": "ABR", "message": "ABR request timed out"}
    except requests.exceptions.RequestException as e:
        return {"success": False, "source": "ABR", "message": f"Network error: {e}"}
    except ET.ParseError as e:
        return {"success": False, "source": "ABR", "message": f"Invalid XML from ABR: {e}"}
    except Exception as e:
        return {"success": False, "source": "ABR", "message": f"Unexpected error: {e}"}


