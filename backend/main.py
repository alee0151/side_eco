def serialize_row(row):
    """
    Convert a database row into a JSON-safe dictionary.

    UUID values are converted to strings because UUID objects are not directly
    JSON serializable.
    """
    if row is None:
        return None
    return {k: str(v) if isinstance(v, UUID) else v for k, v in row.items()}


