def create_search_query(
    cur,
    input_type:  str,
    input_value: str,
    user_id:     Optional[str] = None,
):
    """
    Insert a pending search_query record before executing any external API calls.

    This guarantees a query_id exists even if the pipeline partially fails.
    Status progresses: pending -> resolved | failed
    """
    cur.execute(
        """
        INSERT INTO search_query (user_id, input_type, input_value, resolution_status)
        VALUES (%s, %s, %s, 'pending')
        RETURNING query_id, submitted_at;
        """,
        (user_id, input_type, input_value),
    )
    return cur.fetchone()


def update_search_query(
    cur,
    query_id,
    status:              str,
    resolved_company_id: Optional[str] = None,
    resolved_brand_id:   Optional[str] = None,
    resolved_product_id: Optional[str] = None,
):
    """
    Update search_query resolution after the pipeline completes.
    Only 'resolved' and 'failed' are valid final statuses.
    """
    if status not in ("resolved", "failed"):
        status = "failed"

    cur.execute(
        """
        UPDATE search_query
        SET
            resolution_status   = %s,
            resolved_company_id = %s,
            resolved_brand_id   = %s,
            resolved_product_id = %s
        WHERE query_id = %s;
        """,
        (status, resolved_company_id, resolved_brand_id, resolved_product_id, query_id),
    )


